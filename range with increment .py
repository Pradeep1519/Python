range(5)  # one perameter
# starting point => 0
# condition < 5
# increment => 1

range(1, 6)  # two perameter
# starting point => 1
# condition < 6
# increment => 1

range(1, 9, 2)  # three perameter
# starting point => 1
# condition < 9
# increment => 2 print (1,3,5,8)


for i in range(6):  # one perameter
    print(i)

for n in range(1, 9):  # two perameter
    print(n)

for j in range(1, 9, 2):  # three perameter with increment by 2
    print(j)


