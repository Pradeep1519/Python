l = [1, 2, 3, 4, 5, 6, 7]
print(l[3])  # single argument slicing
print(l[0:6])  # double slicing argument
print(l[0::])  # triple slicing argument
print(l[0::2])  # triple slicing argument with 2 number increment

# reverse slicing
print(l[-3])  # single reverse argument slicing
print(l[-1::-2])  # triple slicing in reverse with decrement with 2 number
print(l[-1::-1]) # print all list with 1 number decrement
