# list is a mutable it means we are change the elements in the list
l = [1, 2, 3, 4, 5, 6, 7]  # list
print(l)  # print the list
print()  # this function will be use only space

# nested list

l1 = [1, 2, 3, 4, 5, 6, [2, 3, 4, 5]]  # this is the nested list
print(l1)
print()  # this function will be use only space

# mix list
l2 = [1, 2, 3, 4, 'hello', [2, 3, 4, 5]]  # this is the mix list
print(l2)
