s1 = 'wellcome to coding world'
# the left to right indexing
print(s1[0])  # print the first indexing element
print(s1[0:8])  # print the 0 to 8 indexing element
print(s1[0::1])  # print the full string with 1 increment

# right to left indexing
print(s1[-1])  # print the last element in the string this is the right to left string
print(s1[-1::-1])  # print the full string in revers and is called right to left print string
